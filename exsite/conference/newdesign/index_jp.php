					<p>現在、民主主義などの共通の価値を基礎とした日印交流が再び活発化しており、両国の首相は2007年を「日印交流年」と宣言し、世界における最も重要な２国間関係であると位置づけています。デリー・ムンバイの産業大動脈プロジェクトや新IIT設立は両国が共同で行う世界的に影響のある二大プロジェクトです。</p>
